
package Operacion;


public class S07_calculadora1 {

    //Metodos

    
    public void sumar(int a, int b){
       int resultado = a + b;
        System.out.println(resultado);
    }
    
     public void resta(int a, int b){
       int resultado = a - b;
        System.out.println(resultado);
    }
    
     public void multiplicacion (int a, int b){
       int resultado = a * b;
        System.out.println(resultado);
    }
     
     public void divicion(int a, int b){
       int resultado = a / b;
        System.out.println(resultado);
    }
    
    
    
}
